package io.markdom.common;

/**
 * A {@link MarkdomListBlockType} indicates the node type of a Markdom list block.
 */
public enum MarkdomListBlockType {

	/**
	 * Indication for a <a href="http://markdom.io/#domain-orderedlistblock"><i>ordered Markdom list block</i></a>.
	 */
	ORDERED,

	/**
	 * Indication for a <a href="http://markdom.io/#domain-unorderedlistblock"><i>unordered Markdom list block</i></a>.
	 */
	UNORDERED;

}